        $this->authLDAPService = $authLDAPService;
    }

    /**
     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function create($data)
    {
        $data =  $this->authLDAPService->auth($data);


        if($data  instanceof \ZF\ApiProblem\ApiProblem){

            if($data->status === 403) {
                return new ApiProblem(403, "Não autorizado");
            } else {
                return $data;
            }
        }

        return $data;
    }

